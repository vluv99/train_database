  CREATE OR REPLACE EDITIONABLE FUNCTION "TRAINDB"."RES_TEST" (start_name IN varchar2, end_name IN varchar2, day DATE) return TRIP_TABLE IS
    results TRIP_TABLE;
BEGIN
    SELECT
        TRIP(
            traindb.menetrend.id,
            traindb.menetrend.nev,
            start_station.neve,
            end_station.neve,
            TRAINDB.tochards(traindb.menetrend.INDUL, 'HHH:MM'),
            TRAINDB.tochards((start_station.time + traindb.menetrend.INDUL), 'HHH:MM'),
            TRAINDB.tochards(end_station.time + traindb.menetrend.INDUL, 'HHH:MM'),
            TRAINDB.tochards(end_station.time - start_station.time, 'HHH:MM'),
            COUNT(ut.id)
        )
    BULK collect INTO results
    FROM
        traindb.menetrend,
        (
            SELECT
                traindb.allomas.neve,
                traindb.megallo.menetrend_id,
                traindb.megallo.time
            FROM
                traindb.megallo,
