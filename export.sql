  CREATE OR REPLACE FORCE EDITIONABLE VIEW "TRAINDB"."TRAINS" ("Train Name", "Station", "ERKEZIK_IDO") AS 
  SELECT
    traindb.menetrend.nev      AS "Train Name",
    traindb.allomas.neve       AS "Station",
    stations.erkezik_ido
FROM
         traindb.menetrend
    INNER JOIN traindb.megallo stations ON traindb.menetrend.id = stations.menetrend_id
    INNER JOIN traindb.allomas ON stations.allomas = traindb.allomas.id
GROUP BY
    traindb.menetrend.nev,
    traindb.allomas.neve,
    stations.erkezik_ido
ORDER BY
    "Train Name",
    stations.erkezik_ido
;
