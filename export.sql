  CREATE OR REPLACE FORCE EDITIONABLE VIEW "TRAINDB"."SZERELVENYEK" ("ID", "MOZDONY", "NUMBER_OF_CARTS", "EXPR1") AS 
  SELECT SZERELVENY.ID,
       SZERELVENY.MOZDONY,
       COUNT(SZERELVENY_KOCSIK.KOCSI) AS NUMBER_OF_CARTS,
       LISTAGG(SZERELVENY_KOCSIK.KOCSI, '; ') WITHIN GROUP (ORDER BY SZERELVENY_KOCSIK.SZERELVENY) "EXPR1"
  FROM SZERELVENY
    INNER JOIN MOZDONY
      ON SZERELVENY.MOZDONY = MOZDONY.ID
    INNER JOIN SZERELVENY_KOCSIK
      ON SZERELVENY_KOCSIK.SZERELVENY = SZERELVENY.ID
    INNER JOIN KOCSI
      ON SZERELVENY_KOCSIK.KOCSI = KOCSI.ID 
group by SZERELVENY.ID, SZERELVENY.MOZDONY
;
